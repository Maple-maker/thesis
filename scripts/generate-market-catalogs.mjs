/**
 * Expands ETF + stock catalogs via Yahoo Finance quote validation.
 *
 * 1. Seeds from scripts/generate-etf-catalog.mjs (manual list)
 * 2. Scans US ticker files (NASDAQ + NYSE) and classifies via quoteType
 *
 * Run: npm run generate:market-catalogs
 * Options:
 *   --delay-ms 2500    pause between Yahoo batches (default)
 *   --batch-size 8     symbols per quote request (default)
 *   --sequential       one symbol per request (slowest, safest)
 *   --max-tickers 0    cap universe (0 = all)
 *   --stocks-only / --etfs-only
 *   --resume           use scripts/.cache/market-quotes.json
 */
import { execSync } from "child_process";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

import {
  classifyEtfAssetClass,
  isUsListed,
  quoteBatch,
  quoteToEtfRow,
  quoteToStockRow,
  sleep,
} from "./lib/market-yahoo.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const etfOut = join(root, "src/data/etf-catalog.generated.ts");
const stockOut = join(root, "src/data/stocks-catalog.generated.ts");
const cachePath = join(__dirname, ".cache/market-quotes.json");

const TICKER_URLS = [
  "https://raw.githubusercontent.com/rreichel3/US-Stock-Symbols/master/nasdaq/nasdaq_tickers.txt",
  "https://raw.githubusercontent.com/rreichel3/US-Stock-Symbols/master/nyse/nyse_tickers.txt",
  "https://raw.githubusercontent.com/rreichel3/US-Stock-Symbols/master/amex/amex_tickers.txt",
];

/** Newer thematic ETFs to always seed (Yahoo-validated when online). */
const EXTRA_ETF_SEEDS = [
  ["AIS", "VistaShares Artificial Intelligence Supercycle ETF", 0.85, "thematic"],
  ["SHLD", "Global X Defense Tech ETF", 0.5, "sector"],
  ["DRAM", "Roundhill Memory ETF", 0.75, "thematic"],
  ["MAGS", "Roundhill Magnificent Seven ETF", 0.29, "thematic"],
];

function parseArgs() {
  const args = process.argv.slice(2);
  const get = (flag, fallback) => {
    const i = args.indexOf(flag);
    return i >= 0 && args[i + 1] ? Number(args[i + 1]) : fallback;
  };
  return {
    delayMs: get("--delay-ms", 2500),
    batchSize: get("--batch-size", 8),
    maxTickers: get("--max-tickers", 0),
    resume: args.includes("--resume"),
    sequential: args.includes("--sequential"),
    stocksOnly: args.includes("--stocks-only"),
    etfsOnly: args.includes("--etfs-only"),
  };
}

function loadManualEtfRows() {
  execSync("node scripts/generate-etf-catalog.mjs", { cwd: root, stdio: "inherit" });
  const raw = readFileSync(etfOut, "utf8");
  const m = raw.match(/export const ETF_CATALOG: EtfCatalogRow\[\] = (\[[\s\S]*?\]);/);
  if (!m) throw new Error("Could not parse etf-catalog.generated.ts");
  return JSON.parse(m[1]);
}

async function fetchTickerUniverse(maxTickers) {
  const symbols = new Set();
  for (const url of TICKER_URLS) {
    const res = await fetch(url);
    if (!res.ok) throw new Error(`Failed ${url}: ${res.status}`);
    const text = await res.text();
    for (const line of text.split("\n")) {
      const sym = line.trim().toUpperCase();
      if (!sym || sym.length > 6) continue;
      if (/[^A-Z0-9.-]/.test(sym)) continue;
      symbols.add(sym);
    }
  }
  let list = [...symbols].sort();
  if (maxTickers > 0) list = list.slice(0, maxTickers);
  return list;
}

function loadCache() {
  if (!existsSync(cachePath)) return {};
  try {
    return JSON.parse(readFileSync(cachePath, "utf8"));
  } catch {
    return {};
  }
}

function saveCache(cache) {
  mkdirSync(dirname(cachePath), { recursive: true });
  writeFileSync(cachePath, JSON.stringify(cache, null, 0));
}

function writeEtfCatalog(rows) {
  const seen = new Set();
  const unique = rows.filter(([sym]) => {
    const s = sym.toUpperCase();
    if (seen.has(s)) return false;
    seen.add(s);
    return true;
  });
  unique.sort((a, b) => a[0].localeCompare(b[0]));

  const ts = `/** AUTO-GENERATED by scripts/generate-market-catalogs.mjs — do not edit by hand */
export type EtfAssetClass =
  | "us-equity"
  | "intl-equity"
  | "fixed-income"
  | "commodity"
  | "real-estate"
  | "sector"
  | "thematic"
  | "factor"
  | "leveraged-inverse";

/** Compact catalog row: symbol, name, expense % or null, asset class, issuer key */
export type EtfCatalogRow = [
  symbol: string,
  name: string,
  expense: number | null,
  assetClass: EtfAssetClass,
  issuer?: string,
];

export const ETF_CATALOG: EtfCatalogRow[] = ${JSON.stringify(unique)};

export const ETF_CATALOG_COUNT = ${unique.length};
`;
  writeFileSync(etfOut, ts, "utf8");
  console.log(`ETFs: ${unique.length} → ${etfOut}`);
}

function writeStockCatalog(rows) {
  const seen = new Set();
  const unique = rows.filter(([sym]) => {
    const s = sym.toUpperCase();
    if (seen.has(s)) return false;
    seen.add(s);
    return true;
  });
  unique.sort((a, b) => a[0].localeCompare(b[0]));

  const ts = `/** AUTO-GENERATED by scripts/generate-market-catalogs.mjs — do not edit by hand */
import type { Sector } from "@/store/types";

/** symbol, name, sector, market cap (USD billions) or null */
export type StockCatalogRow = [symbol: string, name: string, sector: Sector, marketCapB: number | null];

export const STOCK_CATALOG: StockCatalogRow[] = ${JSON.stringify(unique)};

export const STOCK_CATALOG_COUNT = ${unique.length};
`;
  writeFileSync(stockOut, ts, "utf8");
  console.log(`Stocks: ${unique.length} → ${stockOut}`);
}

async function main() {
  const opts = parseArgs();
  const cache = opts.resume ? loadCache() : {};
  const etfMap = new Map();
  const stockMap = new Map();

  if (!opts.stocksOnly) {
    for (const row of loadManualEtfRows()) etfMap.set(row[0], row);
    for (const row of EXTRA_ETF_SEEDS) {
      if (row[0] === "SMCI") continue;
      etfMap.set(row[0], [...row, "other"]);
    }
  }

  const universe = await fetchTickerUniverse(opts.maxTickers);
  const pending = universe.filter((s) => !cache[s]);
  console.log(`Universe: ${universe.length} tickers (${pending.length} to fetch)`);

  let rateLimitStreak = 0;
  for (let i = 0; i < pending.length; i += opts.batchSize) {
    const batch = pending.slice(i, i + opts.batchSize);
    process.stdout.write(`\rYahoo ${Math.min(i + batch.length, pending.length)}/${pending.length}…`);
    let quotes = [];
    try {
      quotes = await quoteBatch(batch, {
        delayMs: opts.delayMs,
        retries: 5,
        chunkSize: opts.batchSize,
        sequential: opts.sequential,
      });
      rateLimitStreak = 0;
    } catch (err) {
      const msg = String(err?.message ?? err);
      if (/too many|429|rate/i.test(msg) && rateLimitStreak < 12) {
        rateLimitStreak++;
        const wait = Math.min(60_000, 5000 * rateLimitStreak);
        console.warn(`\nRate limited — waiting ${wait / 1000}s (streak ${rateLimitStreak})…`);
        saveCache(cache);
        await new Promise((r) => setTimeout(r, wait));
        i -= opts.batchSize;
        continue;
      }
      console.error("\nBatch failed, saving cache:", msg);
      saveCache(cache);
      throw err;
    }

    for (const q of quotes) {
      if (!q?.symbol || !isUsListed(q)) continue;
      cache[q.symbol] = {
        quoteType: q.quoteType,
        name: q.shortName || q.longName,
        sector: q.sector,
        marketCap: q.marketCap,
        expense: q.annualReportExpenseRatio,
      };
      if (q.quoteType === "ETF" && !opts.stocksOnly) {
        etfMap.set(q.symbol, quoteToEtfRow(q));
      } else if (q.quoteType === "EQUITY" && !opts.etfsOnly) {
        stockMap.set(q.symbol, quoteToStockRow(q));
      }
    }
    saveCache(cache);
  }
  console.log("");

  // Apply cache entries not in this run's batches
  for (const [sym, c] of Object.entries(cache)) {
    if (c.quoteType === "ETF" && !opts.stocksOnly && !etfMap.has(sym)) {
      const name = c.name || sym;
      const expense =
        c.expense != null ? Math.round(c.expense * 10000) / 100 : null;
      etfMap.set(sym, [sym, name, expense, classifyEtfAssetClass(name), "other"]);
    }
    if (c.quoteType === "EQUITY" && !opts.etfsOnly && !stockMap.has(sym)) {
      const capB =
        c.marketCap != null ? Math.round((c.marketCap / 1e9) * 10) / 10 : null;
      stockMap.set(sym, [sym, c.name || sym, mapYahooSector(c.sector), capB]);
    }
  }

  if (!opts.stocksOnly) writeEtfCatalog([...etfMap.values()]);
  if (!opts.etfsOnly) writeStockCatalog([...stockMap.values()]);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});

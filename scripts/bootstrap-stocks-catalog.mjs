/**
 * One-time bootstrap: seeds stocks-catalog.generated.ts from curated stocks.ts
 * until a full Yahoo run completes.
 */
import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const stocksPath = join(__dirname, "../src/data/stocks.ts");
const outPath = join(__dirname, "../src/data/stocks-catalog.generated.ts");

const raw = readFileSync(stocksPath, "utf8");
const blocks = raw.split(/\{\s*symbol:/).slice(1);
const rows = [];

for (const block of blocks) {
  const sym = block.match(/"([A-Z0-9.]+)"/)?.[1];
  const name = block.match(/name:\s*"([^"]+)"/)?.[1];
  const sector = block.match(/sector:\s*"([^"]+)"/)?.[1];
  const marketCap = block.match(/marketCap:\s*([\d.]+)/)?.[1];
  if (sym && name && sector) {
    rows.push([sym, name, sector, marketCap ? Number(marketCap) : null]);
  }
}

rows.sort((a, b) => a[0].localeCompare(b[0]));

const ts = `/** AUTO-GENERATED by scripts/bootstrap-stocks-catalog.mjs — replace via generate:market-catalogs */
import type { Sector } from "@/store/types";

/** symbol, name, sector, market cap (USD billions) or null */
export type StockCatalogRow = [symbol: string, name: string, sector: Sector, marketCapB: number | null];

export const STOCK_CATALOG: StockCatalogRow[] = ${JSON.stringify(rows)};

export const STOCK_CATALOG_COUNT = ${rows.length};
`;

writeFileSync(outPath, ts, "utf8");
console.log(`Bootstrap ${rows.length} stocks → ${outPath}`);
